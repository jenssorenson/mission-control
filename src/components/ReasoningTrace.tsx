// ─── Demo/simulated reasoning generator ──────────────────────────────────────

/** Generates a realistic demo reasoning trace for a session */
function generateDemoTrace(sa: SubAgent): ReasoningStep[] {
  const now = Date.now();
  const start = sa.startedAt || (now - 120_000);
  const steps: ReasoningStep[] = [];
  let seq = 0;
  const push = (type: ReasoningStep['type'], label: string, detail?: string, durMs?: number, extra?: Partial<ReasoningStep>): ReasoningStep => ({
    id: `step-${seq++}`,
    timestamp: steps.length === 0 ? start : (steps[steps.length - 1].timestamp + (durMs ?? 800) + Math.floor(Math.random() * 400)),
    type,
    label,
    detail,
    durationMs: durMs ?? (300 + Math.floor(Math.random() * 1200)),
    ...extra,
  });

  steps.push(push('thought', 'Reading task', `Task: "${sa.taskName || 'No description'}"`, 420));
  steps.push(push('plan', 'Planning approach', `Will use ${sa.runtime} runtime to process this request`, 680));
  steps.push(push('thought', 'Checking context', 'Memory check: recent work found in MEMORY.md', 350));

  // Tool calls
  const tools = ['memory_search', 'exec', 'read', 'web_fetch', 'write'];
  const numTools = 2 + Math.floor(Math.random() * 4);
  for (let i = 0; i < numTools; i++) {
    const tool = tools[Math.floor(Math.random() * tools.length)];
    const toolInputs: Record<string, string> = {
      memory_search: `{"query": "recent ${sa.taskName?.split(' ').slice(0, 2).join(' ') ?? 'work'}"}`,
      exec: '{"command": "git status"}',
      read: '{"path": "/Users/jens/.openclaw/workspace/AGENTS.md"}',
      web_fetch: '{"url": "https://api.github.com/repos/openclaw/openclaw"}',
      write: '{"path": "/tmp/output.md", "content": "# Report\\n\\nGenerated by agent"}',
    };
    const toolOutputs: Record<string, string> = {
      memory_search: '{"results": [{"id": "abc", "text": "Jens worked on..."}]}',
      exec: '{"stdout": "On branch main\\nStatus: clean", "exitCode": 0}',
      read: '{"lines": 45, "truncated": false}',
      web_fetch: '{"status": 200, "chars": 2847}',
      write: '{"bytesWritten": 124}',
    };
    steps.push(push('tool_call', `Call: ${tool}`, tool[0].toUpperCase() + tool.slice(1), 150 + Math.floor(Math.random() * 300), {
      toolName: tool,
      toolInput: toolInputs[tool] ?? '{}',
    }));
    steps.push(push('tool_result', `Result: ${tool}`, `${tool} completed successfully`, 200 + Math.floor(Math.random() * 600), {
      toolName: tool,
      toolOutput: toolOutputs[tool] ?? '{"ok": true}',
    }));
  }

  steps.push(push('thought', 'Synthesizing response', 'Combining gathered information into final answer', 920));
  steps.push(push('revision', 'Refining output', 'Improving clarity based on context constraints', 540));
  steps.push(push('completion', 'Task complete', 'All objectives achieved', 310));

  return steps;
}

// ─── Flattened tree with depth ────────────────────────────────────────────────

interface FlatStep {
  step: ReasoningStep;
  depth: number;
}

function flatten(steps: ReasoningStep[], depth = 0): FlatStep[] {
  const result: FlatStep[] = [];
  for (const step of steps) {
    result.push({ step, depth });
    if (step.children?.length) {
      result.push(...flatten(step.children, depth + 1));
    }
  }
  return result;
}

