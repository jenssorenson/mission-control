// ─── Types ────────────────────────────────────────────────────────────────────

export interface ReasoningStep {
  id: string;
  timestamp: number;
  type: 'thought' | 'tool_call' | 'tool_result' | 'plan' | 'revision' | 'completion' | 'error';
  label: string;
  detail?: string;
  toolName?: string;
  toolInput?: string;
  toolOutput?: string;
  durationMs?: number;   // how long this step took
  tokens?: number;       // tokens consumed in this step
  parentId?: string;     // for nested reasoning
  children?: ReasoningStep[];
}

interface Props {
  sessionKey: string;
  subAgents: SubAgent[];
}

// ─── Helpers ──────────────────────────────────────────────────────────────────

function formatAbsTime(ts: number): string {
  return new Date(ts).toLocaleTimeString('en-US', { hour12: false, hour: '2-digit', minute: '2-digit', second: '2-digit', fractionalSecondDigits: 2 });
}

function formatRelTime(ts: number): string {
  const diff = Date.now() - ts;
  const secs = Math.floor(diff / 1000);
  if (secs < 5) return 'just now';
  if (secs < 60) return `${secs}s ago`;
  const mins = Math.floor(secs / 60);
  return `${mins}m ago`;
}

function formatDuration(ms: number): string {
  if (ms < 1000) return `${ms}ms`;
  if (ms < 60000) return `${(ms / 1000).toFixed(1)}s`;
  return `${(ms / 60000).toFixed(1)}m`;
}

