// ─── Stats bar ────────────────────────────────────────────────────────────────

function TraceStats({ steps, sessionKey }: { steps: ReasoningStep[]; sessionKey: string }) {
  const counts: Record<string, number> = {};
  steps.forEach(s => { counts[s.type] = (counts[s.type] ?? 0) + 1; });
  const totalMs = steps.reduce((sum, s) => sum + (s.durationMs ?? 0), 0);
  const toolCalls = steps.filter(s => s.type === 'tool_call').length;
  const toolResults = steps.filter(s => s.type === 'tool_result').length;
  return (
    <div style={{
      display: 'flex', alignItems: 'center', gap: '10px', flexWrap: 'wrap',
      fontSize: '10px', fontFamily: "'Space Grotesk', sans-serif",
    }}>
      <span style={{ display: 'flex', alignItems: 'center', gap: '4px', color: 'var(--text-muted)' }}>
        <span style={{ fontSize: '8px' }}>◉</span> LIVE
      </span>
      <span style={{ color: 'var(--text-muted)' }}>{steps.length} steps</span>
      {toolCalls > 0 && (
        <span style={{ color: 'var(--accent)', fontWeight: 600 }}>
          🔧 {toolCalls} tool call{toolCalls !== 1 ? 's' : ''}
        </span>
      )}
      {toolResults > 0 && (
        <span style={{ color: 'var(--green)' }}>
          📥 {toolResults} result{toolResults !== 1 ? 's' : ''}
        </span>
      )}
      {totalMs > 0 && (
        <span style={{ color: 'var(--text-muted)', fontFamily: "'JetBrains Mono', monospace" }}>
          ⏱ {formatDuration(totalMs)} total
        </span>
      )}
      <span style={{ fontSize: '9px', fontFamily: "'JetBrains Mono', monospace", color: 'var(--text-muted)', marginLeft: 'auto' }}>
        {sessionKey.slice(0, 12)}…
      </span>
    </div>
  );
}

// ─── Main Component ────────────────────────────────────────────────────────────

export default function ReasoningTrace({ sessionKey, subAgents }: Props) {
  const sa = subAgents.find(s => s.sessionKey === sessionKey);

  // Simulated reasoning steps (in a real impl, these would come from gateway events)
  const [steps, setSteps] = useState<ReasoningStep[]>(() => sa ? generateDemoTrace(sa) : []);
  const [copied, setCopied] = useState(false);
  const [filterType, setFilterType] = useState<ReasoningStep['type'] | 'all'>('all');
  const [arrivedCount, setArrivedCount] = useState(0);
  const prevLenRef = useRef(steps.length);

  // Refresh steps when session changes
  useEffect(() => {
    if (sa) {
      setSteps(generateDemoTrace(sa));
      setArrivedCount(0);
      prevLenRef.current = 0;
    }
  }, [sessionKey]);

  // Track new arrivals
  useEffect(() => {
    if (steps.length > prevLenRef.current) {
      setArrivedCount(n => n + (steps.length - prevLenRef.current));
    }
    prevLenRef.current = steps.length;
  }, [steps.length]);

  // Tick relative timestamps
  const [, tick] = useState(0);
  useEffect(() => {
    const t = setInterval(() => tick(n => n + 1), 10000);
    return () => clearInterval(t);
  }, []);

  const handleCopy = async (text: string) => {
    try {
      await navigator.clipboard.writeText(text);
      setCopied(true);
      setTimeout(() => setCopied(false), 1500);
    } catch { /* silent */ }
  };

  const handleCopyAll = async () => {
    const text = steps.map(s =>
      `[${s.timestamp}] [${s.type.toUpperCase()}] ${s.label}${s.detail ? `\n  ${s.detail}` : ''}${s.toolName ? `\n  tool: ${s.toolName}` : ''}${s.durationMs ? `\n  duration: ${formatDuration(s.durationMs)}` : ''}`
    ).join('\n');
    await handleCopy(text);
  };

  const flatSteps = flatten(steps);
  const filtered = filterType === 'all' ? flatSteps : flatSteps.filter(({ step }) => step.type === filterType);

  const types: (ReasoningStep['type'] | 'all')[] = ['all', 'thought', 'plan', 'tool_call', 'tool_result', 'revision', 'completion', 'error'];

  if (!sa) {
    return (
      <div style={{
        padding: '20px', textAlign: 'center', color: 'var(--text-muted)',
        fontFamily: "'Space Grotesk', sans-serif", fontSize: '12px',
      }}>
        Session not found
      </div>
    );
  }

  return (
    <ErrorBoundary name="ReasoningTrace">
      <div className="reasoning-trace-panel" style={{
        display: 'flex', flexDirection: 'column',
        height: '100%', fontFamily: "'Space Grotesk', sans-serif",
      }}>
        {/* Header */}
        <div style={{
          padding: '10px 14px',
          borderBottom: '1px solid var(--border)',
          background: 'var(--bg-card)',
          display: 'flex', flexDirection: 'column', gap: '8px',
        }}>
          <div style={{ display: 'flex', alignItems: 'center', justifyContent: 'space-between' }}>
            <div style={{ display: 'flex', alignItems: 'center', gap: '8px' }}>
              <span style={{ fontSize: '14px' }}>💭</span>
              <span style={{ fontSize: '12px', fontWeight: 700, color: 'var(--text-primary)' }}>
                Reasoning Trace
              </span>
              <span style={{
                fontSize: '9px', fontFamily: "'JetBrains Mono', monospace",
                color: 'var(--accent)', background: 'rgba(59,122,255,0.1)',
                border: '1px solid rgba(59,122,255,0.2)',
                borderRadius: '8px', padding: '1px 6px',
              }}>
                {sa.runtime === 'pi' ? 'Pi' : sa.runtime?.charAt(0).toUpperCase() + sa.runtime?.slice(1)} runtime
              </span>
            </div>
            <div style={{ display: 'flex', gap: '6px' }}>
              <button
                onClick={handleCopyAll}
                title="Copy entire reasoning trace"
                style={{
                  background: copied ? 'rgba(52,211,153,0.15)' : 'rgba(59,122,255,0.08)',
                  border: `1px solid ${copied ? 'rgba(52,211,153,0.3)' : 'rgba(59,122,255,0.2)'}`,
                  borderRadius: '8px', padding: '3px 10px',
                  fontSize: '10px', fontFamily: "'Space Grotesk', sans-serif",
                  color: copied ? 'var(--green)' : 'var(--accent)',
                  cursor: 'pointer', transition: 'all 0.2s',
                }}
              >
                {copied ? '✓ Copied' : '📋 Copy All'}
              </button>
            </div>
          </div>
          <TraceStats steps={steps} sessionKey={sa.sessionKey} />
        </div>

        {/* Filter bar */}
        <div style={{
          display: 'flex', gap: '4px', padding: '6px 14px',
          borderBottom: '1px solid var(--border)',
          overflowX: 'auto', flexShrink: 0,
        }}>
          {types.map(type => {
            const cfg = type === 'all' ? { icon: '◉', color: 'var(--accent)', label: 'All' } : STEP_CONFIG[type];
            const count = type === 'all' ? steps.length : steps.filter(s => s.type === type).length;
            const isActive = filterType === type;
            return (
              <button
                key={type}
                onClick={() => setFilterType(type as ReasoningStep['type'] | 'all')}
                style={{
                  background: isActive ? `${cfg.color}18` : 'transparent',
                  border: `1px solid ${isActive ? `${cfg.color}44` : 'var(--border)'}`,
                  borderRadius: '10px', padding: '2px 8px',
                  fontSize: '10px', fontFamily: "'Space Grotesk', sans-serif",
                  color: isActive ? cfg.color : 'var(--text-muted)',
                  fontWeight: isActive ? 600 : 400,
                  cursor: 'pointer', transition: 'all 0.15s',
                  whiteSpace: 'nowrap',
                  boxShadow: isActive ? `0 0 6px ${cfg.color}22` : 'none',
                }}
              >
                {cfg.icon} {count > 0 && count}
              </button>
            );
          })}
        </div>

        {/* Steps list */}
        <div style={{ flex: 1, overflowY: 'auto', padding: '6px 14px' }}>
          {filtered.length === 0 ? (
            <div style={{ padding: '24px', textAlign: 'center', color: 'var(--text-muted)', fontSize: '11px' }}>
              No reasoning steps yet
            </div>
          ) : (
            filtered.map(({ step, depth }, i) => (
              <StepEntry
                key={step.id}
                step={step}
                depth={depth}
                isNew={i < arrivedCount && arrivedCount > 0}
                onCopy={handleCopy}
              />
            ))
          )}
        </div>

        {/* Footer */}
        <div style={{
          padding: '6px 14px',
          borderTop: '1px solid var(--border)',
          fontSize: '9px', fontFamily: "'JetBrains Mono', monospace",
          color: 'var(--text-muted)',
          display: 'flex', justifyContent: 'space-between',
        }}>
          <span>{filtered.length} step{filtered.length !== 1 ? 's' : ''}{filterType !== 'all' ? ` (${filterType})` : ''}</span>
          <span>Click any step to expand details</span>
        </div>
      </div>
    </ErrorBoundary>
  );
}
